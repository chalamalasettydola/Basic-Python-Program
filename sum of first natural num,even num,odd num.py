#Sum of the first natural numbers,even numbers,odd numbers
a=int(input("Enter the number : "))
sum_natural = a*(a + 1)// 2
sum_even = a*(a + 1)
sum_odd = a*a
print("Sum of first", a, "natural numbers:", sum_natural)
print("Sum of first", a, "even numbers:", sum_even)
print("Sum of first", a, "odd numbers:", sum_odd)
